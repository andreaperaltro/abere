interface Rect { top: number; left: number; bottom: number; right: number }

function overlaps(a: Rect, b: Rect): boolean {
  return !(a.right <= b.left || a.left >= b.right || a.bottom <= b.top || a.top >= b.bottom);
}

/** Collect blocked zones: actual visual text bounds + SVGs + other icons.
 *
 *  We use the Range API (getClientRects) for text elements so we get the
 *  actual rendered line boxes, not the full-width block-element boxes.
 *  This leaves the right margin and the top area free when text is short.
 */
// getBoundingClientRect() is viewport-relative; for absolute positioning we
// add window.scrollY so zones map to document coordinates.
function blockedZones(
  otherIcons: Array<{ top: number; left: number }>,
  pad = 16,
): Rect[] {
  const zones: Rect[] = [];
  const scrollY = window.scrollY;
  const container = document.querySelector("[data-content-zone]");

  if (container) {
    container.querySelectorAll("p, a, button").forEach((el) => {
      try {
        const range = document.createRange();
        range.selectNodeContents(el);
        for (const r of Array.from(range.getClientRects())) {
          if (r.width > 4 && r.height > 4) {
            zones.push({
              top: r.top + scrollY - pad,
              left: r.left - pad,
              bottom: r.bottom + scrollY + pad,
              right: r.right + pad,
            });
          }
        }
      } catch (_) { /* ignore */ }
    });

    container.querySelectorAll("svg").forEach((el) => {
      const r = (el as SVGSVGElement).getBoundingClientRect();
      if (r.width > 0 && r.height > 0) {
        zones.push({
          top: r.top + scrollY - pad,
          left: r.left - pad,
          bottom: r.bottom + scrollY + pad,
          right: r.right + pad,
        });
      }
    });
  }

  const iconSize = getIconSize();
  for (const p of otherIcons) {
    zones.push({
      top: p.top - pad,
      left: p.left - pad,
      bottom: p.top + iconSize + pad,
      right: p.left + iconSize + pad,
    });
  }

  return zones;
}

// Quadrants: 0=TL, 1=TR, 2=BL, 3=BR
// Pick any quadrant that isn't already occupied
function freeQuadrant(occupied: number): number {
  const available = [0, 1, 2, 3].filter((q) => q !== occupied);
  return available[Math.floor(Math.random() * available.length)];
}

function findSafePos(
  otherIcons: Array<{ top: number; left: number }>,
  quadrant: number
): { top: number; left: number } {
  const w = window.innerWidth;
  const h = window.innerHeight;
  const scrollY = window.scrollY;
  const size = getIconSize();
  const isMobile = size < 150;
  const zones = blockedZones(otherIcons, isMobile ? 8 : 16);

  const hw = w / 2;
  const hh = h / 2;
  const qLeft = (quadrant === 1 || quadrant === 3) ? hw : 0;
  const qTop  = (quadrant === 2 || quadrant === 3) ? scrollY + hh : scrollY;

  const tryPos = (top: number, left: number) => {
    const rect: Rect = { top, left, bottom: top + size, right: left + size };
    return !zones.some((z) => overlaps(rect, z));
  };

  // 1. Try within the assigned quadrant
  for (let i = 0; i < 100; i++) {
    const pos = {
      top:  qTop  + Math.random() * Math.max(hh - size, 0),
      left: qLeft + Math.random() * Math.max(hw - size, 0),
    };
    if (tryPos(pos.top, pos.left)) return pos;
  }

  // 2. Quadrant too full — try anywhere in the visible viewport
  for (let i = 0; i < 100; i++) {
    const pos = {
      top:  scrollY + Math.random() * Math.max(h - size, 0),
      left: Math.random() * Math.max(w - size, 0),
    };
    if (tryPos(pos.top, pos.left)) return pos;
  }

  // 3. Last resort: scan a grid for the first free cell
  const step = size / 2;
  for (let top = scrollY; top < scrollY + h - size; top += step) {
    for (let left = 0; left < w - size; left += step) {
      if (tryPos(top, left)) return { top, left };
    }
  }

  // 4. Absolute fallback: off-screen (invisible, better than overlapping text)
  return { top: scrollY - size * 2, left: 0 };
}

interface ActiveIcon {
  id: number;
  iconIndex: number;
  pos: { top: number; left: number };
  rotation: number;
  size: number;
  quadrant: number;
  startDelay: number; // ms before draw begins
}

let _uid = 0;
let _iconSeq = 0;

// ─── Single icon animator ─────────────────────────────────────────────────────

function SingleIcon({
  icon,
  onComplete,
  onDrawStart,
  startDelay = 0,
}: {
  icon: IconDef;
  onComplete: () => void;
  onDrawStart: () => void;
  startDelay?: number;
}) {
  const svgRef = useRef<SVGSVGElement>(null);
  const onCompleteRef = useRef(onComplete);
  const onDrawStartRef = useRef(onDrawStart);
  onCompleteRef.current = onComplete;
  onDrawStartRef.current = onDrawStart;

  useEffect(() => {
    const svg = svgRef.current;
    if (!svg) return;

    const paths = Array.from(svg.querySelectorAll<SVGPathElement>("path"));
    const lengths = paths.map((p) => p.getTotalLength());

    paths.forEach((p, i) => {
      p.style.fill = "none";
      p.style.strokeDasharray = `${lengths[i]} ${lengths[i]}`;
      p.style.strokeDashoffset = String(lengths[i]);
    });

    let rafId = 0;
    let tid: ReturnType<typeof setTimeout>;
    let cancelled = false;

    const runDraw = () => {
      onDrawStartRef.current();
      const t0 = performance.now();
      const frame = (now: number) => {
        if (cancelled) return;
        const t = Math.min((now - t0) / DRAW_MS, 1);
        paths.forEach((p, i) => {
          p.style.strokeDashoffset = String(lengths[i] * (1 - easeOutCubic(t)));
        });
        if (t < 1) {
          rafId = requestAnimationFrame(frame);
        } else {
          tid = setTimeout(runErase, HOLD_MS);
        }
      };
      rafId = requestAnimationFrame(frame);
    };

    const runErase = () => {
      if (cancelled) return;
      const t0 = performance.now();
      const frame = (now: number) => {
        if (cancelled) return;
        const t = Math.min((now - t0) / ERASE_MS, 1);
        paths.forEach((p, i) => {
          p.style.strokeDashoffset = String(-lengths[i] * easeInOutCubic(t));
        });
        if (t < 1) {
          rafId = requestAnimationFrame(frame);
        } else {
          onCompleteRef.current();
        }
      };
      rafId = requestAnimationFrame(frame);
    };

    tid = setTimeout(runDraw, startDelay);
    return () => {
      cancelled = true;
      cancelAnimationFrame(rafId);
      clearTimeout(tid);
    };
  }, [startDelay]);

  return (
    <svg
      ref={svgRef}
      viewBox={icon.viewBox}
      className="w-full h-full"
      xmlns="http://www.w3.org/2000/svg"
    >
      {icon.paths.map((p, i) => (
        <path
          key={i}
          d={p.d}
          fill="none"
          stroke={p.stroke}
          strokeWidth={p.strokeWidth}
          strokeLinecap="round"
          strokeLinejoin="round"
        />
      ))}
    </svg>
  );
}

// ─── Exported component ───────────────────────────────────────────────────────
// Two icons run concurrently. Positions are computed after mount so the DOM is
// available for collision detection. Icons avoid [data-content-zone] elements
// and each other.

const OFF_SCREEN = { top: -1000, left: -1000 };

export default function AnimatedIcons() {
  // Track when each icon's draw actually began (by id), so we can compute stagger delays
  const drawStartTimes = useRef<Map<number, number>>(new Map());

  const [icons, setIcons] = useState<ActiveIcon[]>(() => {
    _uid = 2;
    _iconSeq = 2;
    const q0 = Math.floor(Math.random() * 4);
    const q1 = freeQuadrant(q0);
    return [
      { id: 0, iconIndex: 0, pos: OFF_SCREEN, rotation: randomRotation(), size: 200, quadrant: q0, startDelay: 0 },
      { id: 1, iconIndex: 1, pos: OFF_SCREEN, rotation: randomRotation(), size: 200, quadrant: q1, startDelay: DRAW_MS / 2 },
    ];
  });

  useEffect(() => {
    const size = getIconSize();
    setIcons((prev) => {
      const pos0 = findSafePos([], prev[0].quadrant);
      const pos1 = findSafePos([pos0], prev[1].quadrant);
      return [
        { ...prev[0], pos: pos0, size },
        { ...prev[1], pos: pos1, size },
      ];
    });
  }, []);

  const handleDrawStart = useCallback((id: number) => {
    drawStartTimes.current.set(id, performance.now());
  }, []);

  const handleComplete = useCallback((id: number) => {
    setIcons((prev) => {
      const other = prev.find((ic) => ic.id !== id);
      const otherQuadrant = other?.quadrant ?? 0;
      const newQuadrant = freeQuadrant(otherQuadrant);
      const others = other ? [other.pos] : [];
      const iconIndex = _iconSeq % ICONS_ORDER.length;
      _iconSeq++;

      // Compute startDelay so the new icon begins drawing when the surviving
      // icon reaches 50% of its draw phase. If the surviving icon is already
      // past that point, start immediately.
      let startDelay = 0;
      if (other) {
        const drewAt = drawStartTimes.current.get(other.id);
        if (drewAt !== undefined) {
          const elapsed = performance.now() - drewAt;
          startDelay = Math.max(0, DRAW_MS / 2 - elapsed);
        }
      }

      // Clean up the completed icon's timestamp
      drawStartTimes.current.delete(id);

      return prev.map((ic) =>
        ic.id === id
          ? {
              id: _uid++,
              iconIndex,
              pos: findSafePos(others, newQuadrant),
              rotation: randomRotation(),
              size: getIconSize(),
              quadrant: newQuadrant,
              startDelay,
            }
          : ic
      );
    });
  }, []);

  return (
    <>
      {icons.map((ic) => (
        <div
          key={ic.id}
          className="absolute pointer-events-none"
          style={{
            top: ic.pos.top,
            left: ic.pos.left,
            width: ic.size,
            height: ic.size,
            zIndex: 0,
            transform: `rotate(${ic.rotation}deg)`,
            transformOrigin: "50% 50%",
          }}
        >
          <SingleIcon
            icon={ICONS_ORDER[ic.iconIndex]}
            onComplete={() => handleComplete(ic.id)}
            onDrawStart={() => handleDrawStart(ic.id)}
            startDelay={ic.startDelay}
          />
        </div>
      ))}
    </>
  );
}
